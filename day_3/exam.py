# To be approved in the exam, the student must obtain a grade greater than or equal to 7.0 and have at least 75% attendance.
# Thise are the student's average grade and attendance rate:
#   average_grade = 8.5
#   attendance_rate = 80%
# Write a program that prints whether the student has been approved or not.

# Variables
average_grade = 8.5
attendance_rate = 80

# Conditions
if average_grade >= 7.0 and attendance_rate >= 75:
    print("The student has been approved.")
else:
    print("The student has not been approved.")