# Python Arithmetic Operators

# variables
a = 10
b = 5

# sum
sum = a + b
print(sum)

# subtraction
sub = a - b
print(sub)

# multiplication
mult = a * b
print(mult)

# division
div = a / b
print(div)

# modulus
mod = a % b
print(mod)

# exponent
exp = a ** b
print(exp)

# floor division
fdiv = a // b
print(fdiv)

